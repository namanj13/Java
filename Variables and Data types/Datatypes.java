public class Datatypes {
    public static void main(String[] args) {
        byte b = 20; // it has a smaller range only 256 byte type ke variable hi store ho skte h 
        System.out.println(b);
        char ch = 'b';   // hum akele single character hi store kr skte h char mein 
        System.out.println(ch);
        boolean var = false; 
        System.out.println(var);
        float price = 10.5f; // by default decimal point value double hoti h isliye f lagana padta h float ke liye 
        System.out.println(price);
        int number = 99;
        System.out.println(number);
        // LONG jb use krte h jab int ki range se jyadha bada number use kr rhe ho
        // DOUBLE hum jb use krte hai jb floating ya decimal value bht bada numnber ho 
        short n = 250; // short int se chota hota h
        System.out.println(n);
    }
}
